	<h4>About Us</h4>

	<p>
		TEACH (TEACH Education for All Comprehensive Help) is a social enterprise that operates through a mobile-friendly website providing a low-cost service to answer academic queries, targeted mainly at less-fortunate students who cannot afford expensive tuition services.
	</p>

	<p>
		Students can post mathematics and science questions, which can either be conceptual or assessment in nature, at primary, secondary or junior college level and a small fee will be deducted from their account balance upon posting.
	</p>

	<p>
		Solutions will be provided within 3 days (with 12- or 24-hour priority upgrades available), which are concise step-by-step explanations written to the Singapore examination standards, namely PSLE, “O”, “N” and “A” level.
	</p>

	<p>
		Our organization consists of a small proportion of full-time employees (who will be paid a fixed monthly salary), part-time employees (who will be paid a 70% commission of the revenue generated from questions they answered), and unpaid volunteers. All staff will be interviewed to ensure their competency in their fields.
	</p>

	<p>
		Part-time tutors and volunteers can browse through all unanswered questions and choose any question they want to answer on a first-come-first-serve basis. Full-time tutors serve as a safety net in the event that questions are not taken up by part-time tutors and volunteers.
	</p>
